// 버전 API를 통해 다시 검색·노출되지 않도록 공개 문자열만 중립화한다.
const RETIRED_PATCH_NOTE_TERMS = [
  /eng-exam-prep/gi,
  /korean-lit-exam/gi,
  /phys-mock-exam/gi,
  /phys-inquiry/gi,
  /math-inquiry/gi,
  /영어 시험대비(?:\s*3종|\s*세트)?/g,
  /국어 문학 시험(?:\s*세트)?/g,
  /물리 모의고사/g,
  /(?:물리|수학|물리[·/]수학|수학[·/]물리) 수행평가/g,
];

function sanitizePatchNoteText(value) {
  return RETIRED_PATCH_NOTE_TERMS.reduce(
    (text, pattern) => text.replace(pattern, "미출시 기능"),
    String(value || ""),
  );
}

const PATCH_NOTES = RAW_PATCH_NOTES.map((entry) => ({
  ...entry,
  title: sanitizePatchNoteText(entry.title),
  items: entry.items.map(sanitizePatchNoteText),
}));

const serverStartedAt = new Date().toISOString();

function readGitCommit() {
  const fromEnv =
    process.env.RENDER_GIT_COMMIT ||
    process.env.GIT_COMMIT ||
    process.env.COMMIT_SHA ||
    process.env.SOURCE_VERSION ||
    process.env.VERCEL_GIT_COMMIT_SHA;
  if (fromEnv) return fromEnv.trim();

  try {
    return execSync("git rev-parse HEAD", {
      cwd: path.join(__dirname, ".."),
      stdio: ["ignore", "pipe", "ignore"],
      encoding: "utf8",
      timeout: 1000,
    }).trim();
  } catch {
    return "";
  }
}

function readGitBranch() {
  const fromEnv =
    process.env.RENDER_GIT_BRANCH ||
    process.env.GIT_BRANCH ||
    process.env.BRANCH ||
    process.env.VERCEL_GIT_COMMIT_REF;
  if (fromEnv) return fromEnv.trim();

  try {
    return execSync("git rev-parse --abbrev-ref HEAD", {
      cwd: path.join(__dirname, ".."),
      stdio: ["ignore", "pipe", "ignore"],
      encoding: "utf8",
      timeout: 1000,
    }).trim();
  } catch {
    return "";
  }
}

function readGitNumber(format, fallback = 0) {
  try {
    const value = execSync(`git ${format}`, {
      cwd: path.join(__dirname, ".."),
      stdio: ["ignore", "pipe", "ignore"],
      encoding: "utf8",
      timeout: 1000,
    }).trim();
    const number = Number(value);
    return Number.isFinite(number) && number > 0 ? Math.floor(number) : fallback;
  } catch {
    return fallback;
  }
}

function pullRequestNumberFromSubject(subject) {
  const text = String(subject || "").trim();
  const explicit = text.match(/(?:pull request|PR)\s*#(\d+)/i);
  if (explicit) return Number(explicit[1]) || 0;
  const squash = text.match(/\(#(\d+)\)\s*$/);
  return squash ? Number(squash[1]) || 0 : 0;
}

function readPullRequestNumber() {
  try {
    const subject = execSync("git show -s --format=%s HEAD", {
      cwd: path.join(__dirname, ".."),
      stdio: ["ignore", "pipe", "ignore"],
      encoding: "utf8",
      timeout: 1000,
    }).trim();
    return pullRequestNumberFromSubject(subject);
  } catch {
    return 0;
  }
}

function buildReleaseVersion() {
  const parts = String(packageJson.version || "1.0.0").split(".").map((value) => Number(value) || 0);
  const major = parts[0] || 1;
  const minor = parts[1] || 0;
  const patch = parts[2] || 0;
  const pullRequest = readPullRequestNumber();
  const commitCount = readGitNumber("rev-list --count HEAD");
  const commitEpoch = readGitNumber("show -s --format=%ct HEAD");
  const build = pullRequest || (commitCount > 1 ? commitCount : patch);
  return {
    releaseVersion: `${major}.${minor}.${Math.max(patch, build)}`,
    releaseBuild: build,
    releaseSource: pullRequest ? "pull-request" : commitCount > 1 ? "commit-count" : commitEpoch ? "commit" : "package",
  };
}

function readPackageUpdatedAt() {
  try {
    return fs.statSync(path.join(__dirname, "..", "package.json")).mtime.toISOString();
  } catch {
    return "";
  }
}

function getVersionInfo({ includeNotes = false } = {}) {
  const commit = readGitCommit();
  const info = {
    app: packageJson.name,
    version: packageJson.version,
    commit,
    shortCommit: commit ? commit.slice(0, 7) : "",
    branch: readGitBranch(),
    serverStartedAt,
    packageUpdatedAt: readPackageUpdatedAt(),
    environment: process.env.NODE_ENV || "development",
    ...buildReleaseVersion(),
  };
  if (includeNotes) info.patchNotes = PATCH_NOTES;
  return info;
}

